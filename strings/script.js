// string method : allows you to manipulate and work with texts

// using charAt
let username = "Penguin";

console.log(username.charAt(2));

// using indexOf
console.log(username.indexOf("g"));

// using length
console.log(username.length);

// using trim
console.log(username.trim());

// using toUpperCase
console.log(username.toUpperCase());

// using toLowerCase
console.log(username.toLowerCase());

//using repeat
console.log(username.repeat(6));

// using startsWith
let answers = username.startsWith(" ");
console.log(answers);
